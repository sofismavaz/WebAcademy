package BackEnd;

public class exerc_tipo {
    public static void main(String[] args) {
        int numero = 10;
        double decimal = 5.5;
        String texto = "Olá, Mundo!";
        boolean verdadeiroFalso = true;
        float numeroFloat = 3.14f;

        System.out.println("Número inteiro: " + numero);
        System.out.println("Número decimal: " + decimal);
        System.out.println("Texto: " + texto);
        System.out.println("Valor booleano: " + verdadeiroFalso);
        System.out.println("Número float: " + numeroFloat);
    }
}
